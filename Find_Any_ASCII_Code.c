// Find ASCII Code of the characters +,-,*,/,A

#include <stdio.h>
int main(){
    char x ='+';
    printf("ASCII code of + is %d \n",x);

    char y = '-';
    printf("ASCII code of - is %d  \n",y);

    char z = '*';
    printf("ASCII code of * is %d \n",z);

    char a = '/';
    printf("ASCII code of / is %d \n",a);

    char b ='A';
    printf("ASCII code of A is %d \n",b);
}